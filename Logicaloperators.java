package javaprograms;

public class Logicaloperators {
	public static void main(String a[]) {
		//Logical Operator
				//&&-and, ||-or, !-not
					boolean j=true;
					boolean k=false;
					
					System.out.println((j && k));
					System.out.println((j||k));
					System.out.println(!(j));
	}
}