package javaprograms;

public class unaryoperators {
	//-Unary Operator(++)
	public static void main(String a[]) {
		int a1=25,b=8;
	//Post Increment
	System.out.println("% values is"+(a1++));
	//Pre Increment
	System.out.println("% values is"+(++a1));
	
	//PostDecrement
			System.out.println("% values is"+(b--));
			
			//PreDecrement
			System.out.println("% values is"+(--b));
	
}
}