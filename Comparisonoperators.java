package javaprograms;

public class Comparisonoperators {
	//Comparison operators
	public static void main(String a[]) {
	int g=20,h=30,i=40;
	//>-greater than
	System.out.println((h>g));
	//<-lesser than
	System.out.println((h<g));
	//==-equal to
	System.out.println((h==g));
	//>=-greater than equal to
	System.out.println((h>=g));
	//<=-lesser than equal to
	System.out.println((h<=g));
}
}